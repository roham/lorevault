// Get questions filtered by difficulty
export function getQuestionsByDifficulty(difficulty: TriviaQuestion['difficulty']): TriviaQuestion[] {
  return TRIVIA_QUESTIONS.filter(q => q.difficulty === difficulty);
}

// Shuffle and pick N questions, weighted by difficulty
export function pickTriviaQuestions(count: number, seed: number): TriviaQuestion[] {
  // Seeded shuffle
  let s = seed;
  const next = () => {
    s = (s * 16807) % 2147483647;
    return (s - 1) / 2147483646;
  };

  const shuffled = [...TRIVIA_QUESTIONS].sort(() => next() - 0.5);
  return shuffled.slice(0, count);
}
